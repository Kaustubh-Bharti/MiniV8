// Arithmetic
console.log("Add:", 10 + 5);
console.log("Sub:", 10 - 5);
console.log("Mul:", 10 * 5);
console.log("Div:", 10 / 5);
console.log("Mod:", 10 % 3);
console.log("Pow:", 2 ** 10);

// Comparison
console.log("5 == 5:", 5 == 5);
console.log("5 === 5:", 5 === 5);
console.log("5 != 3:", 5 != 3);
console.log("5 > 3:", 5 > 3);
console.log("5 < 3:", 5 < 3);
console.log("5 >= 5:", 5 >= 5);
console.log("5 <= 3:", 5 <= 3);

// Logical
console.log("true && false:", true && false);
console.log("true || false:", true || false);
console.log("!true:", !true);

// Ternary
let age = 20;
let status = age >= 18 ? "adult" : "minor";
console.log("Status:", status);

// Compound assignment
let x = 10;
x += 5;
console.log("+=:", x);
x -= 3;
console.log("-=:", x);
x *= 2;
console.log("*=:", x);
x /= 4;
console.log("/=:", x);
x %= 5;
console.log("%=:", x);
